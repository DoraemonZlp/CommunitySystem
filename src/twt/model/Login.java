package twt.model;

public class Login {
 private int id;
 private String password;
 private int type;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public int getType() {
	return type;
}
public void setType(int type) {
	this.type = type;
}
 
}
