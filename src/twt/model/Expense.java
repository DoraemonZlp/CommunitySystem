package twt.model;


public class Expense {

    private int id;

    private int yearmonth;

    private int water;

    private int watered;

    private int electricity;

    private int electricityed;

    private int gas;

    private int gased;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getYearmonth() {
        return yearmonth;
    }
    public void setYearmonth(int yearmonth) {
        this.yearmonth = yearmonth;
    }
    public int getWater() {
        return water;
    }
    public void setWater(int water) {
        this.water = water;
    }
    public int getWatered() {
        return watered;
    }
    public void setWatered(int watered) {
        this.watered = watered;
    }
    public int getElectricity() {
        return electricity;
    }
    public void setElectricity(int electricity) {
        this.electricity = electricity;
    }
    public int getElectricityed() {
        return electricityed;
    }
    public void setElectricityed(int electricityed) {
        this.electricityed = electricityed;
    }
    public int getGas() {
        return gas;
    }
    public void setGas(int gas) {
        this.gas = gas;
    }
    public int getGased() {
        return gased;
    }
    public void setGased(int gased) {
        this.gased = gased;
    }

}